        public static string getCompanyAscription(string cood)
        {
            string city = null;
            int length = cood.Length;
            switch (length)
            {
                case 15:
                    city = cood.Substring(0, 6);
                    if (!cityCodeMap.TryGetValue(city, out city))
                    {
                        city = cood.Substring(0, 7);
                    }
                    break;
                case 18:
                    city = cood.Substring(2, 6);
                    if (!cityCodeMap.TryGetValue(city, out city))
                    {
                        city = cood.Substring(2, 7);
                    }
                    break;
                case 16:
                case 17:
                default:
                    throw new Exception("企业三证合一或者工商注册号码错误！");
            }
            return city;
        }

